$name = "Gustavo Casagrande";
print($name);
print("<br>");

// Variáveis em php são pouco tipadas. Uma variável pode receber texto e posteriormente receber um inteiro, boolean, float etc.
$name = 123;
print($name);
print("<br>");

$value = "Gustavo";
print ("Name: $value");
